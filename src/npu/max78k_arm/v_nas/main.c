void softmax_baseline(const float *vec_in, const uint16_t dim_vec, float *p_out)
{
    float sum = 0.0f;
    int16_t i;

    for (i = 0; i < dim_vec; i++)
    {
        p_out[i] = expf(vec_in[i]);
        sum += p_out[i];
    }

    for (i = 0; i < dim_vec; i++)
    {
        p_out[i] /= sum;
    }
}


